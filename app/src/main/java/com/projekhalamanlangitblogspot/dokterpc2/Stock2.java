package com.projekhalamanlangitblogspot.dokterpc2;


public class Stock2 {
    String namatoko;
    String link;
    String stson;

    public Stock2(String namatoko, String link, String stson) {
        this.namatoko = namatoko;
        this.link = link;
        this.stson = stson;
    }

    public String getNama() {
        return namatoko;
    }

    public String getLink() {
        return link;
    }

    public String getSts() {
        return stson;
    }


}
